#!/usr/bin/env tsx
/**
 * WHOOP API Data Exploration Script
 *
 * First run:  opens browser for WHOOP OAuth, stores tokens in .env.local
 * Later runs: uses stored tokens, auto-refreshes if expired
 *
 * Usage: npm run whoop:explore
 * Output: whoop-data-dump.json (raw responses from all endpoints)
 */

import * as fs from 'node:fs';
import * as http from 'node:http';
import * as path from 'node:path';
import { exec } from 'node:child_process';
import { promisify } from 'node:util';

const execAsync = promisify(exec);

// ---------------------------------------------------------------------------
// Config
// ---------------------------------------------------------------------------

const ENV_FILE = path.join(process.cwd(), '.env.local');
const DUMP_FILE = path.join(process.cwd(), 'whoop-data-dump.json');
const API_BASE = 'https://api.prod.whoop.com/developer';
const AUTH_URL = 'https://api.prod.whoop.com/oauth/oauth2/auth';
const TOKEN_URL = 'https://api.prod.whoop.com/oauth/oauth2/token';
const OAUTH_SCOPES =
  'read:recovery read:cycles read:workout read:sleep read:profile read:body_measurement offline';

// ---------------------------------------------------------------------------
// .env.local helpers
// ---------------------------------------------------------------------------

function readEnv(): Record<string, string> {
  if (!fs.existsSync(ENV_FILE)) return {};
  const env: Record<string, string> = {};
  for (const line of fs.readFileSync(ENV_FILE, 'utf-8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;
    env[trimmed.slice(0, eqIdx).trim()] = trimmed.slice(eqIdx + 1).trim();
  }
  return env;
}

function upsertEnvKey(content: string, key: string, value: string): string {
  const lines = content.split('\n');
  const idx = lines.findIndex((l) => l.trim().startsWith(`${key}=`));
  if (idx >= 0) {
    lines[idx] = `${key}=${value}`;
    return lines.join('\n');
  }
  const sectionIdx = lines.findIndex((l) => l.includes('# WHOOP'));
  if (sectionIdx >= 0) {
    lines.splice(sectionIdx + 1, 0, `${key}=${value}`);
  } else {
    if (lines[lines.length - 1].trim() !== '') lines.push('');
    lines.push('# WHOOP — generated by whoop-explore.ts', `${key}=${value}`);
  }
  return lines.join('\n');
}

function writeEnvKeys(updates: Record<string, string>): void {
  let content = fs.existsSync(ENV_FILE) ? fs.readFileSync(ENV_FILE, 'utf-8') : '';
  for (const [k, v] of Object.entries(updates)) content = upsertEnvKey(content, k, v);
  fs.writeFileSync(ENV_FILE, content);
}

